// string to Number 
let stringNumber = "0o233"
let NumberConverted = Number(stringNumber)
console.log(typeof NumberConverted)
console.log( NumberConverted)

// >> "233" -----> number
// >> "23ww3" -----> NaN  not a number
// >> true -----> 1
// >> false -----> 0
// >> 0x2332 -----> hexadecimal
// >> 0o2332 -----> octal 
// >> 0b1101 -----> binary


